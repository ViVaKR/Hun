.global _test_array_sum
.align 2

// -------------------------------------------------------------
// pub fn asm_test_array_sum(ptr: *const i64, len: i64) -> i64
// 입력: x0 = 배열의 시작 주소 포인터, x1 = 배열의 길이 (요소 개수)
// 출력: x0 = 모든 요소의 합산 결과
// -------------------------------------------------------------

// 일반 레이블 언더바 (_)
// 링커나 디버거가 다른 파일에서도 찾아 올 수 있도록 
// **심볼 테이블** 이라는 족보에 이름을 등록해 둠
_test_array_sum:
  mov x2, #0        // sum = 0, x2 = 누적 합산(sum) 을 0으로 초기화

// 로컬 레이블 
// 만약 `loop_body:` 라고 적어두면 결과는? 
// 이름 충돌 : 다른 함수에서 또 쓰면 이름이 겹친다고 빌드 에러(Redefinition) 가 날수 있음.
// 메모리 족보 낭비 : 최종 실행 파일의 심볼 테이블이 수만개의 루프 지점으로 꽉차서 용량늘고 지저분해짐
// `.L` 로 시작하면 이것은 이 파일 안에서만 쓰고 버릴 진짜 사소한 지역 변수라는 의미
// `.L_loop_body:` 장성:  어셈블러가 빌드 시점에 적절한  주소 offset 으로 싹 변환한 뒤,
// 심볼 테이블에서는 아예 흔적도 없이 지워버림
.L_loop_body:
  cbz x1, .L_loop_done  // x1(남은길이)이 0이면 루프 즉시 종료!
  // 초과학 기술: x0 주소에서 8바이트(i64)를 x3에 로드한 뒤,
  // x0 포인터를 자동으로 8바이트 만큼 전진 시킴 (Post-Index)
  ldr x3, [x0], #8    // x3 = *x0, x0 += 8
  add x2, x2, x3      // sum += x3, sum(x2) = sum(x2) + x3
  sub x1, x1, #1      // len -= 1, len(x1) = len(x1) - 1
  b .L_loop_body     // 다음 루프로 점프

.L_loop_done:
  mov x0, x2          // x0 = sum, 결과값(sum)을 반환 레지스터 x0에 장착!
  ret 