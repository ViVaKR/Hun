// -------------------------------------------------------------
// [영역 1] 전역 심볼 선언 (Global Symbols)
// -------------------------------------------------------------
.global _main

// -------------------------------------------------------------
// [영역 2] 읽기 전용 데이터 섹션 (코드 및 문자열 상수 구역)
// __TEXT: 수정 불가능한 구역, __cstring: 문자열 리터럴 전용 공간
// -------------------------------------------------------------
.section __TEXT,__cstring,cstring_literals

// -------------------------------------------------------------
// [영역 3] 읽고 쓰기가 가능한 데이터 섹션 (변수 및 배열 구역)
// __DATA: 읽고 쓰기 가능, __data: 초기화된 글로벌/스태틱 변수 공간
// -------------------------------------------------------------
.section __DATA,__data
format_str:
    .asciz "Hello, ARM64 Assembly! ==> %d\n"

// -------------------------------------------------------------
// [영역 4] 진짜 명령어가 실행되는 코드 섹션 (Code 구역)
// __TEXT: 실행 가능한 구역, __text: 실제 기계어 코드가 위치하는 공간
// -------------------------------------------------------------
.section __TEXT,__text,regular,pure_instructions

.extern _atoi

.align 2
_main:

    // -------------------------------------------------------------
    // [프롤로그] 표준 29/30 레지스터 및
    // 로컬 저장을 위한 Callee-saved 레지스터(x19, x20) 백업
    // -------------------------------------------------------------
    stp x29, x30, [sp, #-32]!   // 32 바이트 공간 확보 (16바이트 정렬 유지)
    stp x19, x20 [sp, #16]      // x19, x20 백업 (함수 호출 간 값 보존용)
    mov x29, sp

    // 1. 인자의 개수(argc) 검사: 최소 2개 이상이어야 함 (실행파일명 + 인자1)
    cmp x0, #2
    b.lt _terminate             // 인자가 부족하면 바로 종료

    // 2. argv[1] 가져오기 및 atoi 변환
    ldr x0, [x1, #8]        // x0 = argv[1]
    bl _atoi                // atoi(argv[1]) 호출 -> 결과는 x0에 저장됨
    mov x19, x0             // 결과를 안전하게 x19(Calle-saved)에 보관

    // 3. 포맷 스트링 및 인자 준비 후 printf 호출
    adrp x0, format_str@PAGE  // 포맷 문자열 주소 로드
    add x0, x0, format_str@PAGEOFF
    mov x1, x19
    // str x1, [sp, #-16]!
    bl _printf              // _printf 호출

// -------------------------------------------------------------
// 종료 (에필로그 및 리턴)
// -------------------------------------------------------------
_terminate:
    mov x0, #0                      // 리턴값 0
    ldp x19, x20, [sp, #16]         // 보존 레지스터 복원
    ldp x29, x30, [sp], #32         // 프레임 포인터, 링크 레지스터 복원 및 스택 정리
    ret


// --------------------------------- //
    // cmp x0, #2      // argc < 2 ?
    // b.lt _terminate

    // argv[1] 가져오기
    // 포인터 하나가 64bit = 8byte
    // os 가 _main 을 호출할 때
    // x0 = argc
    // x1 = argv
    // x2 = envp (플랫폼에 따라)

