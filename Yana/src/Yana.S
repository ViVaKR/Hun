.include "../include/section_macros.inc"

// -------------------------------------------------------------
// [영역 1] 전역 심볼 선언 (Global Symbols)
// -------------------------------------------------------------
.global _main

// -------------------------------------------------------------
// [문자열 리터럴] __TEXT,__cstring,cstring_literals
// printf 포맷 스트링 등 널 종료(null-terminated) 문자열 상수 전용
// 읽기 전용 데이터섹션, 수정 불가능한 구역
// -------------------------------------------------------------
CSTRING_SECTION

// -------------------------------------------------------------
// [읽기전용 상수 데이터] __TEXT,__const
// 문자열이 아닌 일반 상수 (룩업 테이블, 상수 배열 등) - 재배치 불필요
// -------------------------------------------------------------
CONST_SECTION


// -------------------------------------------------------------
// [초기화된 변수] __DATA,__data
// int a = 10; 처럼 선언과 동시에 값이 있는 전역/정적 변수
// 읽고 쓰기 가능, 초기화된 글로벌/정적 변수 공간
// -------------------------------------------------------------
DATA_SECTION
.align 3
format_str: .asciz "Hello, ARM64 Assembly! ==> %d\n"
.align 3
command_table:
    .quad _playground       // x10 + 0  (x19가 0일 때)
    .quad _fib              // x10 + 8  (x19가 1일 때 1 * 2^3)
    .quad _quicksort        // x10 + 16 (x19가 2일 때 2 * 2^3)
command_table_end:

// 섹션 지시어와 상관없이, 계산식 직후에 두는 것이 가독성에 가장 좋음.
// 왜 여기에 두는것이 최선인가? 
// 어셈블러는 위에서 아래로 코드를 읽으며 주소를 계산함
// command_table 시작 주소와 command_table_end 주소를 다 파악한 직후인 바로 다음 줄에
// .equ 계산식을 두어야 어셈블러가 헷갈맂 않고 정확하게 숫자를 뽑아맨.
// .equ NUM_COMMANDS, 4
// .equ NUM_COMMANDS, (command_table_end - command_table) / 8
// -------------------------------------------------------------
// [0으로 초기화된 변수] __DATA,__bss
// int a; 처럼 초기값이 없거나 명시적으로 0인 전역/정적 변수
// 실제 바이너리 용량은 차지 안 하고, 로드 시점에 커널이 0으로 채워줌
// -------------------------------------------------------------
BSS_SECTION

// -------------------------------------------------------------
// [4바이트 실수 상수] __TEXT,__literal4
// float(32비트) 리터럴 전용. 링커 자동 중복 제거
// -------------------------------------------------------------
LITERAL4_SECTION

// -------------------------------------------------------------
// [8바이트 실수 상수] __TEXT,__literal8
// double(64비트) 리터럴 전용. 링커 자동 중복 제거
// -------------------------------------------------------------
LITERAL8_SECTION

// -------------------------------------------------------------
// [잠정 정의/공용 심볼] __DATA,__common
// 여러 오브젝트 파일에서 동일 이름의 전역 변수를 "선언"만 하고
// 링커가 최종적으로 하나로 병합해주는 구역 (C의 tentative definition에 대응)
// 어셈블리에서 직접 쓸 일은 드물지만, C 코드와 링크할 때 마주칠 수 있음
// -------------------------------------------------------------
COMMON_SECTION

// -------------------------------------------------------------
// [코드 구역] __TEXT,__text
// 실제 기계어 명령어가 위치. pure_instructions = 이 섹션엔 데이터 없음(순수 명령어)
// -------------------------------------------------------------
CODE_SECTION

.extern _atoi

.align 2
_main:

    // -------------------------------------------------------------
    // [프롤로그] 표준 29/30 레지스터 및
    // 로컬 저장을 위한 Callee-saved 레지스터(x19, x20) 백업
    // -------------------------------------------------------------
    stp x29, x30, [sp, #-32]!   // 32 바이트 공간 확보 (16바이트 정렬 유지)
    stp x19, x20, [sp, #16]      // x19, x20 백업 (함수 호출 간 값 보존용)
    mov x29, sp

    // 1. 인자의 개수(argc) 검사: 최소 2개 이상이어야 함 (실행파일명 + 인자1)
    cmp x0, #2
    b.lt _terminate             // 인자가 부족하면 바로 종료

    // 2. argv[1] 가져오기 및 atoi 변환
    ldr x0, [x1, #8]        // x0 = argv[1]
    bl _atoi                // atoi(argv[1]) 호출 -> 결과는 x0에 저장됨
    mov x19, x0             // 결과를 안전하게 x19(Calle-saved)에 보관



    // -------------------------------------------------------------
    // [C#의 생성자 역할] 런타임에 동적으로 개수 계산하기!
    // -------------------------------------------------------------

    // 1. 테이블 시작 주소(x10)와 끝 주소(x11) 계산
    adrp x10, command_table@PAGE
    add x10, x10, command_table@PAGEOFF // x10 = command_table 시작 주소

    adrp x11, command_table_end@PAGE
    add x11, x11, command_table_end@PAGEOFF  // x11 = command_tabel 끝 주소

    // 2. 동적 개수 계산 (x12 = 개수)
    sub x12, x11, x10
    lsr x12, x12, #3

    // 3. 64비트끼리 안전하게 범위 검사 (Boundary Check)
    cmp x19, x12             // 범위검사 (Boundary Check) : 사용자가 입력한 아규먼트 값 과 배열의 수를 비교 
    b.hs _default           // w19 가 배열의 크기보다 크거나 같으면(hs: Higher or Same, 부호없는 비교) _default 레이블로 점프

    // 진짜 함수 주로 로드하기
    // x10 (테이블 시작 주소)에서 x19(인덱스) 만큼 8바이트씩(lsl #3) 떠러진 곳의 주소값을 읽어와 
    // x11을 덮어쓰기
    ldr x11, [x10, x19, lsl #3]

    // 목표 함수 실행!
    blr x11

    b _terminate

_default:
    // 3. 포맷 스트링 및 인자 준비 후 printf 호출
    adrp x0, format_str@PAGE  // 포맷 문자열 주소 로드
    add x0, x0, format_str@PAGEOFF
    
    sub sp, sp, #32
    str x19, [sp, #0]
    bl _printf              // _printf 호출
    add sp, sp, #32
    b _terminate


// -------------------------------------------------------------
// 종료 (에필로그 및 리턴)
// -------------------------------------------------------------
_terminate:
    mov x0, #0                      // 리턴값 0
    ldp x19, x20, [sp, #16]         // 보존 레지스터 복원
    ldp x29, x30, [sp], #32         // 프레임 포인터, 링크 레지스터 복원 및 스택 정리
    ret
